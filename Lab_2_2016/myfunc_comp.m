function [f, C0, C, Cab, y, CKO_proc] = myfunc_comp ( N, M, T, kp)            
    C0 = 0;    
    f=zeros(1,N+1);
    for i=1:N+1 %????????? ????????? ???????       
%         f(i)=sin(2*pi*kp*(i-1)/N); % ????????????? ???????  
%         f(i) = pi*kp*(i-1)/N
f(i) = (2*T*(((i-1-N/2))/N));
        C0=C0+f(i);
    end

    C0=C0*(2/N);
    for k=1:M
       C(k)=0; 
    end
    
    for i=1:N+1
        for k=1:M
        C(k)=C(k)+f(i)*exp(-j*2*pi*k*(i-1)/N);     
        end
    end
    
    for k=1:M
        C(k)=C(k)*(2/N);
    end 
    
    for k=1:M 
        Cab(k)=abs(C(k));%???????????? Cab(k)- ??????????? ????? ???? a+jb,     
    end
    
    %?????????? ? ??????????? ??????? ???????? (?????) 
    for i=1:N+1
        y(i)=0;
        
        for k=1:M    
            y(i)=y(i)+C(k)*exp(j*2*pi*k*(i-1)/N);   
        end
        y(i)=C0/2+y(i);
    end

    
    for i=2:N
       dy(i)=real(y(i))-f(i);  
    end
    
    dy_proc=dy/(max(f)-min(f))*100;
    CKO=std(dy);
    CKO_proc=std(dy_proc)%СКО в процентах
 
end